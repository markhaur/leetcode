func TestFindMaxConsecutiveOnes(t *testing.T) {
	tt := []struct {
		Nums     []int
		Expected int
	}{
		{
			Nums:     []int{1, 1, 0, 1, 1, 1},
			Expected: 3,
		},
		{
			Nums:     []int{1, 0, 1, 1, 0, 1},
			Expected: 2,
		},
		{
			Nums:     []int{0, 0, 0, 0, 0, 0, 0},
			Expected: 0,
		},
	}

	for _, tc := range tt {
		assert.Equal(t, findMaxConsecutiveOnes(tc.Nums), tc.Expected)
	}
}

